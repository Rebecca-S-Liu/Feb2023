namespace ConsoleApp2;

public static class Addition
{
    public static int AddNumbers(int a, int b)
    {
        return a + b;
    }

    public static double AddNumbers(double a, double b)
    {
        return a + b;
    }

    public static int AddNumbers(int a, int b, int c)
    {
        return a + b + c;
    } 

}